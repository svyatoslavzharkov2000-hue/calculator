# РАСЧЁТЫ
# -----------------------------
price = nominal * price_percent / 100
coupon_money = nominal * coupon_percent / 100

current_yield = coupon_money / price * 100

ytm = (
    (coupon_money + (nominal - price) / years)
    /
    ((nominal + price) / 2)
) * 100

total_income = coupon_money * years + nominal - price
total_percent = total_income / price * 100
real_yield = ytm - inflation

